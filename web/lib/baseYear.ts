// ===========================
// 基準年度（年度＝4月始まり）の算出
// lib/baseYear.ts
//
// 「問題作成時に基準とした最新資料の年度」を1か所で定義する。
// ・プロンプト（lib/prompts/*）が AI に提示する基準年度
// ・ストック保存（lib/stock.ts）が questions.base_year に記録する値
// この2つを必ず同じ計算に統一するための単一の真実。
//
// 税制改正・学習指導要領の改訂は年度（4月1日始まり）で切り替わるため、
// 暦年ではなく「年度」を採用する（1〜3月は前年度扱い）。
// ===========================

/**
 * 基準年度を返す（年度＝4月始まり）。
 * 例：2026年5月→2026、2026年2月→2025（＝2025年度）。
 */
export function getBaseYear(date: Date = new Date()): number {
    // getMonth() は0始まり。3 = 4月。4月以降はその年、1〜3月は前年。
    return date.getMonth() >= 3 ? date.getFullYear() : date.getFullYear() - 1
}
