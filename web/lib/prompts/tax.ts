// ===========================
// 税法4択問題プロンプト
// prompts/tax.ts
//
// 税理士試験および税務実務（TKC方式の月次監査・申告実務を含む）に資する
// 4択問題の生成プロンプトをここで管理する。
//
// ★表記ルール本文（NOTATION_RULES）は、JSONを壊さないための鉄則と、
//   金額・税率・条文・日付など税法特有の表記を「誤 → 正」で統一する。
//   ルールを直すときはここだけ直せばよい。
// ===========================

import { getBaseYear } from '@/lib/baseYear'

/** 4択問題のJSON出力スキーマ定義 */
const TAX_OUTPUT_SCHEMA = `
    {
    "questions": [
        {
        "id": 1,
        "unitId": "単元ID（下の【単元ID対応表】のidのいずれかを必ず使う）",
        "question": "問題文",
        "choices": {
            "A": "選択肢A",
            "B": "選択肢B",
            "C": "選択肢C",
            "D": "選択肢D"
        },
        "answer": "A",
        "explanation": "解説文（なぜその答えになるかを、根拠となる制度・要件にふれて説明）",
        "keywords": ["キーワード1", "キーワード2", "キーワード3"]
        }
    ]
    }
`.trim()

// ============================================================
// 表記ルール本文（税目共通・全例を「誤 → 正」で統一）
//
// 並び順は「JSONを壊す致命度が高い順 → 表示の安定度」。
//   1) JSONを壊さない（最優先）
//   2) 金額・数値・割合（税率）の表記
//   3) 条文・法令名・用語の表記
//   4) スペースの入れ方
// ============================================================
export const NOTATION_RULES = `
        【最重要・JSONを壊さないための鉄則】
        - 出力はJSONとして完全に妥当であること。文字列値の途中で改行しない（1つの値は1行で書く）
        - 問題文・解説・選択肢の中でダブルクォート(")は絶対に使わない
          ・語句や用語の引用はダブルクォートではなくカギ括弧「」を使う
            （例：誤 "課税事業者" となる → 正 「課税事業者」となる）
        - $ 記号やバックスラッシュ(\\\\)によるLaTeX記法は絶対に使わない
        - バッククォート(\`)やMarkdownのコード記法は絶対に使わない。数式・計算式もプレーンテキストで書く
        - choices の各値は、金額・割合・数値であっても必ずダブルクォートで囲んだ文字列にする
          （例：誤 "A": 1000000 → 正 "A": "1,000,000円"）
          （例：誤 "A": 10% → 正 "A": "10%"）
        - answer は "A" "B" "C" "D" のいずれかの文字列にする
        - keywords は必ずダブルクォートで囲んだ文字列の配列にする
          （例：誤 [仕入税額控除, インボイス] → 正 ["仕入税額控除", "インボイス", "適格請求書"]）

        【金額・数値・割合（税率）の表記】
        - 金額は半角数字で書き、3桁ごとにカンマを入れ、末尾に「円」を付ける
          （例：誤 1000000円、100万 → 正 1,000,000円。文中で概数を述べる場合のみ「100万円」も可）
        - 税率・割合は半角数字＋半角の % で書く（全角％は使わない）
          （例：誤 １０％、10 ％ → 正 10%）
        - 計算式の演算子（+ - × ÷ =）は半角で書き、前後に半角スペースを入れる
          （例：誤 1000000×10%=100000 → 正 1,000,000円 × 10% = 100,000円）
        - 分数・割合は「3分の2」または「2/3」のように書く（LaTeXの分数記法は使わない）
        - 期間・年数は半角数字＋単位で詰めて書く（例：3年、6か月、2か月以内）

        【条文・法令名・用語の表記】
        - 条文を引用するときは「消費税法第30条」「所得税法第57条」のように、法令名＋条番号で書く
          ・項・号まで示すときは「第30条第1項」「第2条第1項第9号」のように続けて書く
          ・条文番号はあいまいな場合や確信が持てない場合は無理に書かず、制度名・要件で説明する
            （誤った条番号を断定して書かないこと。正確さを最優先する）
        - 法令名の略称は一般的なものに統一する（例：措置法＝租税特別措置法、通則法＝国税通則法）
        - 制度名・専門用語は正式名称で書く
          （例：誤 インボイス → 正 適格請求書（インボイス）。初出で正式名称、以降は略称可）
          （例：小規模宅地等の特例、配偶者の税額軽減、基礎控除、損益通算 などは正式名称で）
        - 年度・改正への言及は、設問が前提とする年（後述の基準年）で施行されている内容に統一する
          ・改正の経過措置や将来施行分を混同しないこと

        【スペースの入れ方】
        - 数値と単位（円・%・年・月・日・割・分）の間にスペースを入れない
          （例：誤 100,000 円、10 % → 正 100,000円、10%）
        - 文頭・句読点・カギ括弧「」・全角括弧（）の前後にはスペースを入れない
        - 日本語文中の括弧は全角（）を用いること（数式・計算式の中の半角括弧 () は可）
`.trim()

/** 税法4択問題の生成プロンプトを組み立てる */
export function buildTaxPrompt(
    subjectLabel: string,
    units: { id: string; label: string }[],
    questionCount: number,
): string {
    // 基準年度（年度＝4月始まり）。ストック保存時の base_year と同じ値を使い、
    // 「どの年度の税制を基準に作られた問題か」をプロンプトと保存で一致させる。
    const baseYear = getBaseYear()
    const unitText =
        units.length > 0
        ? units.map((u) => u.label).join('・')
        : `${subjectLabel}の全単元（ランダム）`

    // 単元ID対応表：AIにはこのidのいずれかを unitId として返させる（ラベル照合に依存しない）。
    const unitIdTable =
        units.length > 0
            ? units.map((u) => `          - ${u.id} ： ${u.label}`).join('\n')
            : '          （単元指定なし。最も適切な単元を自分で判断し、その単元のidは付けず空文字 "" を返す）'

    return `
        あなたは税理士試験および税務実務（TKC方式の月次巡回監査・申告実務を含む）に精通した、${subjectLabel}の4択問題を作成する専門家です。

        以下の条件で問題を${questionCount}問生成してください。

        【条件】
        - 対象：${subjectLabel}（${baseYear}年度＝${baseYear}年4月1日〜${baseYear + 1}年3月31日に適用される日本の税法に準拠。
          直近の税制改正を反映し、将来施行分・経過措置のものと混同しないこと）
        - 形式：4択選択問題（正解はちょうど1つ）
        - 単元：${unitText}
        - 想定利用者：税理士・税理士試験受験生・会計事務所職員。出題は税理士試験の基礎〜標準レベル、
          かつ実務（月次監査・決算申告）で押さえるべき論点を中心にする
        - 指定された税目・単元の範囲を厳守すること。範囲外の発展論点や他税目の内容を混在させない
        - 制度・要件・金額基準・税率は正確であること。確信が持てない数値や条番号は断定して書かず、
          制度の趣旨・要件で説明する（不正確な暗記事項を作り出さない）
        - 同じ問題を繰り返さず、毎回異なる論点・切り口の問題を生成すること
        - 各問題に、その問題で問われている主要な制度・論点を表す keywords を3〜5個付けること
          （復習の手がかりに使う。例：「仕入税額控除」「適格請求書」「帳簿及び請求書等の保存」）

        【問題文(question)の作り方】
        - question には「問い」だけを書く。選択肢A〜Dの文章を question の中に列挙・再掲しないこと
        - 設問の形は「正しいものを一つ選べ」「誤っているものを一つ選べ」など明確にする
          （「誤っているもの」を選ばせる場合は、その旨を問題文にはっきり書く）
        - 事例問題では、前提（事業者の区分・課税期間・金額など）を簡潔に示してから問う

        【選択肢(choices)の作り方】
        - 各選択肢の本文の先頭に、番号や記号（（１）(1) ① 1. ア． A. 等）を付けないこと。
          choices には「選択肢の文そのもの」だけを入れる（番号は表示時にアプリ側が自動で振る）
        - A〜Dの4つの選択肢は、すべて内容の異なる別の文にすること（実質同一の選択肢を作らない）
        - 正解はちょうど1つにすること
        - 誤答（ダミー）は、実務・受験でありがちな誤解に基づくもっともらしい別内容にする
          （要件の一部だけ変える・適用対象を取り違える・金額や率を典型的な誤りに差し替える 等）

        【解説(explanation)の作り方】
        - 文体は簡潔な敬体（です・ます）で書く。口語・雑談・感想・呼びかけを使わないこと
        - 長さは目安として日本語で120〜250字程度。「なぜその選択肢が正解か」を、根拠となる
          制度・要件にふれて簡潔にまとめる
        - 計算問題は、計算の要点（適用する率・控除額・順序）を示す。冗長な途中式の羅列は避ける
        - 誤答選択肢の解説は、必要な場合のみ1行程度で簡潔に触れる（全選択肢を逐一長く論評しない）

        【単元ID対応表（unitId は必ずこの id のいずれかを使う）】
${unitIdTable}
        - 各問題の unitId には、上の表の id（左側の英数字。例 tax_consumption_04）を**そのままの表記で**入れること
        - 単元名（ラベル）ではなく id を入れること。表に無い id は使わないこと
        - 複数単元が指定されている場合は、その問題が実際に該当する単元の id を選ぶこと

        【記法の厳守事項（重要）】
${NOTATION_RULES}

        【出力形式】
        必ず以下のJSON形式のみで出力すること。
        前後に説明文・コードブロック記号は不要。すべての文字列値はダブルクォートで囲むこと。

        ${TAX_OUTPUT_SCHEMA}
    `.trim()
}
